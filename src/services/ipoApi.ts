export async function fetchIpoData(): Promise<IpoResponse> {
    // Simulate network delay
    await new Promise(resolve => setTimeout(resolve, 300));

    const now = new Date().toLocaleString('en-IN', { timeZone: 'Asia/Kolkata' });

    return {
        open: allIpoData.filter(ipo => ipo.status === 'Open'),
        upcoming: allIpoData.filter(ipo => ipo.status === 'Upcoming'),
        closed: allIpoData.filter(ipo => ipo.status === 'Closed'),
        lastUpdated: now,
    };
}

export async function fetchIpoBySlug(slug: string): Promise<IpoDetails | null> {
    await new Promise(resolve => setTimeout(resolve, 200));
    return allIpoData.find(ipo => ipo.slug === slug) || null;
}

export async function fetchAllIpos(): Promise<IpoDetails[]> {
    await new Promise(resolve => setTimeout(resolve, 200));
    return allIpoData;
}

export function getAllIpoSlugs(): string[] {
    return allIpoData.map(ipo => ipo.slug);
}

export default { fetchIpoData, fetchIpoBySlug, fetchAllIpos, getAllIpoSlugs };
