function replaceObjectByIdRegex(contentStr, targetId, replacement) {
    let idIndex = contentStr.indexOf('id: ' + targetId + ',');
    if (idIndex === -1) return contentStr;

    let start = contentStr.lastIndexOf('{', idIndex);

    let braceCount = 1;
    let end = start + 1;
    while (braceCount > 0 && end < contentStr.length) {
        if (contentStr[end] === '{') braceCount++;
        if (contentStr[end] === '}') braceCount--;
        end++;
    }

    return contentStr.substring(0, start) + replacement + contentStr.substring(end);
}

