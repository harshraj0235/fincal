/** Latest-first dates (2026-02-06 14:00 down to 05:10, 10 min apart) so #1 shows at top */
function latestFirstISO(index: number): string {
  const base = new Date('2026-02-06T14:00:00+05:30').getTime();
  return new Date(base - index * 10 * 60 * 1000).toISOString();
}

